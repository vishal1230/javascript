const month=3

switch(month){ // we can pass string as key also in switch cases
    case 1:
        console.log("month is jan")
        break;
    case 2:
        console.log("month is feb")
        break;
    case 3:
        console.log("month is march")
        break;
    default:
        console.log("default case executed")
        break;        
}